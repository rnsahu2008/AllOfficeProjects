package jsonManupulation;

public class Car 
{
    private String brand1 ;
    private int    doors ;
    private int   win ;

    public String getBrand() {
		return brand1;
	}
	public void setBrand(String brand1) {
		this.brand1 = brand1;
	}
	public int getDoors() {
		return doors;
	}
	public void setDoors(int doors) {
		this.doors = doors;
	}
	public int getWin() {
		return win;
	}
	public void setWin(int win) {
		this.win = win;
	}
}